package wcci.org.pawsclaws.Enums;

/**
 * Enumeration representing the different types of pets.
 * <p>
 * This enum defines the available pet types that the system recognizes, including
 * both standard pets (e.g., Dog, Cat) and robotic versions (e.g., RoboticDog, RoboticCat).
 * </p>
 */
public enum PetType {
    /**
     * Represents a standard dog.
     */
    Dog,

    /**
     * Represents a standard cat.
     */
    Cat,

    /**
     * Represents a robotic dog.
     */
    RoboticDog,

    /**
     * Represents a robotic cat.
     */
    RoboticCat
}
